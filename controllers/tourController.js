exports.getAllTours = async (req, res) => {
  try {
    const tours = await Tour.find(); //find method without any argument will read all the data it will return an array

    res.status(200).json({
      status: 'success',
      requestedAt: req.requestTime,
      results: tours.length,
      data: {
        tours
      }
    });
  } catch (err) {
    res.status(404).json({
      status: 'failed',
      message: err
    });
  }
};

exports.getTour = async (req, res) => {
  try {
    const tour = await Tour.findById(req.params.id);
    //Tour.findOne( {_id : req.params.id})  it is a mongodb method which works same as above line (underscore id because in mongodb id looks like underscore id)
    res.status(200).json({
      status: 'success',
      data: {
        tour
      }
    });
  } catch (err) {
    res.status(404).json({
      status: 'failed',
      message: err
    });
  }
};

